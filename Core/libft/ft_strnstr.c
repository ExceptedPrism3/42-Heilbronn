#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	if (little[0] == '\0')
		return ((char *)big);
	i = 0;
	while (i < len && big[i] != '\0')
	{
		j = 0;
		while (little[j] != '\0' && (i + j < len) && big[i + j] == little[j])
			j++;
		if (little[j] == '\0')
			return ((char *)big + i);
		i++;
	}
	return (NULL);
}

/*Inner loop: check for a match.
Ths condition checks everything at once:
- Is there more of 'little' to compare? (little[j])
- Are we still within the 'len' boundary? (i + j < len)
- Do the characters match? (big[i + j] == little[j])*/