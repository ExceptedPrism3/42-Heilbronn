#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char letter)
{
	write(1, &letter, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

