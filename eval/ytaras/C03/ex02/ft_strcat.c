char	*ft_strcat(char *dest, char *src)
{
	int	i;

	i = 0;
	while (*dest != '\0')
	{
		++dest;
	}
	while (*src != '\0')
	{
		*(dest + i) = *src;
		++src;
		i++;
	}
	return (dest);
}
