int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (*s1 != 0 || *s2 != '\0')
	{
		if (*s1 != *s2)
		{
			i = *s1 - *s2;
			break ;
		}
		++s1;
		++s2;
	}
	return (i);
}
